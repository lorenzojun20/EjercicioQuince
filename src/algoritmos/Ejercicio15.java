    private void cmdCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCalcularActionPerformed
        String total, ord, esp;
        double prest, porc, op1 = 0, op2 = 0, op3 = 0;

        if (txtPrestamo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite Valor del Préstamo", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtPrestamo.requestFocusInWindow();
        } else {
            prest = Double.parseDouble(txtPrestamo.getText());
            porc = 24;
            op1 = ((prest * porc) / 100) + prest;
            op2 = (op1 / 2) / 4;
            op3 = (op1 / 2) / 20;
        }
        total = String.valueOf(op1);
        txtTotal.setText("$" + total);
        esp = String.valueOf(op2);
        txtCuotasEsp.setText("$" + esp);
        ord = String.valueOf(op3);
        txtCuotasOrd.setText("$" + ord);

    }//GEN-LAST:event_cmdCalcularActionPerformed

